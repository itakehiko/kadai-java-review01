package lesson1_4;

public class ArithmeticOperations {

    public static void main(String[] args) {
        
        // result 1
        int num1 = 201;
        int num2 = 10;
        int result = num1 + num2;
        System.out.println("num1 + num2 = " + result);
        
        // result 2
        result = num1 - num2;
        System.out.println("num1 - num2 = " + result);
        
        // result 3
        result = num1 * num2;
        System.out.println("num1 * num2 = " + result);
        
        // result 4
        result = num1 / num2;
        System.out.println("num1 / num2 = " + result);
        
        // result5
        result = num1 % num2;
        System.out.println("num1 % num2 = " + result);
        
    }

}
